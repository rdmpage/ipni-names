function id_to_filename($id)
{
	global $basedir;
	
	$rdf_id = $id;
	$rdf_id = preg_replace('/-\d+$/', '', $rdf_id);

	$dir = floor($rdf_id / 1000);
	
	$filename = $basedir . '/' . $dir . '/' . $id . '.xml';
	
	return $filename;
}


//----------------------------------------------------------------------------------------
function rdf_to_sql($Id)
{	
	$filename = id_to_filename($Id);

	$rdf = file_get_contents($filename);

	// Fix IPNI bug
	$rdf = preg_replace('/ & /', ' &amp; ', $rdf);

	$obj = new stdclass;
	
	$obj->team = array();

	// extract authorship
	$dom= new DOMDocument;
	$dom->loadXML($rdf);
	$xpath = new DOMXPath($dom);

	$xpath->registerNamespace("rdf", 		"http://www.w3.org/1999/02/22-rdf-syntax-ns#");
	$xpath->registerNamespace("dc", 		"http://purl.org/dc/elements/1.1/" );
	$xpath->registerNamespace("dcterms", 	"http://purl.org/dc/terms/");
	$xpath->registerNamespace("tn", 		"http://rs.tdwg.org/ontology/voc/TaxonName#");
	$xpath->registerNamespace("tm", 		"http://rs.tdwg.org/ontology/voc/Team#" );
	$xpath->registerNamespace("tcom", 		"http://rs.tdwg.org/ontology/voc/Common#" );
	$xpath->registerNamespace("p", 			"http://rs.tdwg.org/ontology/voc/Person#" );
	$xpath->registerNamespace("owl", 		"http://www.w3.org/2002/07/owl#" );


	$nodeCollection = $xpath->query ("//tn:TaxonName/@rdf:about");
	foreach($nodeCollection as $node)
	{
		$obj->Id = $node->firstChild->nodeValue;
		$obj->Id = str_replace('urn:lsid:ipni.org:names:', '', $obj->Id);
		$obj->Id = preg_replace('/:(.*)$/', '', $obj->Id);
	}


	//------------------------ authors -----------------------------------------------
	$nodeCollection = $xpath->query ("//tn:authorship");
	foreach($nodeCollection as $node)
	{
		$obj->authorship = $node->firstChild->nodeValue;
	}		


	/*
	<tm:Team>
	<tm:name>Gagnon, G.P.Lewis &amp; C.E.Hughes</tm:name>
	<tm:hasMember rdf:resource="urn:lsid:ipni.org:authors:20028451-1"
	*/


	$nodeCollection = $xpath->query ("//tm:Team/tm:hasMember");
	foreach($nodeCollection as $node)
	{
		$member = new stdclass;

		$nc = $xpath->query ('@rdf:resource', $node);
		foreach($nc as $n)
		{
			$member->author_id = str_replace("urn:lsid:ipni.org:authors:", "", $n->firstChild->nodeValue);
		}

		$nc = $xpath->query ('@tm:index', $node);
		foreach($nc as $n)
		{
			$member->index = $n->firstChild->nodeValue;
		}

		$nc = $xpath->query ('@tm:role', $node);
		foreach($nc as $n)
		{
			$member->role = $n->firstChild->nodeValue;
		}

		if (isset($member->author_id))
		{			
			$obj->team[] = $member;
		}
	}


	//print_r($obj);


	foreach ($obj->team as $team)
	{

		$keys = array();
		$values = array();

		$keys[] = 'Id';
		$values[] = '"' . $obj->Id . '"';

		foreach ($team as $k => $v)
		{
			$keys[] = "`" . $k . "`";
			$values[] = '"' . addcslashes($v, '"') . '"';
		}

		$sql = 'INSERT IGNORE INTO ipni_authors(' . join(',', $keys) . ') VALUES (' . join(',', $values) . ');' . "\n";

		echo $sql;
	}
}

